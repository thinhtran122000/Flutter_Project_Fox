// Switch - case - break - default (Dùng để kiểm tra điều kiện của các trường hợp)
// ignore_for_file: avoid_print

void main(List <String> args){
  String? name;
  switch (name){
    case 'Fox':
      print('Thinh');
      break;
    case 'Winter':
      print('Quan');
      break;
    case 'Ryuk':
      print('Viet');
      break;
    default: print('Unknown');
  } 
}